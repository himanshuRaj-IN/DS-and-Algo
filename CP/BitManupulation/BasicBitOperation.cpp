#include <bits/stdc++.h>
using namespace std;

int main() {
    // Testing various Bit Manupulation Operator
    // 1. Bitwise and < & > 
    cout << (8 & 11) << endl;
    // 2. Bitwise Or < | >
    cout << (8 | 11) << endl;
    // 3. Bitwise Not < ~ > 
    cout << (~0) << endl;
    //4. Bitwise LeftShift Operator < << > 
    cout << (11<<2) << endl;
    //5. Bitwise RightShife Operator < >> >
    cout << (10>>2) << endl;

    
    return 0;
}